//methods in array (inbuilt in js)
arr2 = [12, 14, 56, 57];
console.log(arr2);

// pop method
var c = arr2.pop();
console.log(c);
console.log('poped array ' + arr2);


//push method
arr2.push(100);
console.log('pushed array ' + arr2);


//shift method
var d = arr2.shift();
console.log(d);
console.log('shifted array ' + arr2);


//unshift method
arr2.unshift(102);
console.log('unshifted array ' + arr2);